// Provider brand marks for runtimes Citadel surfaces in the cockpit. These
// are the providers' own SVGs (Claude via Wikipedia Commons, Codex and Cursor
// supplied by the operator); they identify the product to the user. Nominative
// use only — the marks are trademarks of their respective owners.
export function RuntimeMark({ runtimeId, size = 14 }: { runtimeId: string; size?: number }) {
  switch (runtimeId) {
    case "claude-code":
      return <ClaudeMark size={size} />;
    case "codex":
      return <CodexMark size={size} />;
    case "cursor-agent":
      return <CursorMark size={size} />;
    default:
      return <GenericMark runtimeId={runtimeId} size={size} />;
  }
}

